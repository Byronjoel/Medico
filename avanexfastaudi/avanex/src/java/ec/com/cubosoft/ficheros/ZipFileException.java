package ec.com.cubosoft.ficheros;

/**
 *
 * @author JUAN PABLO 3 <jp3@cubosoft.com.ec>
 */
public class ZipFileException extends Exception {
    private static final long serialVersionUID = 1L;

    /**
     * Creates a new instance of <code>ZipFileException</code> without detail message.
     */
    public ZipFileException() {
    }


    /**
     * Constructs an instance of <code>ZipFileException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public ZipFileException(String msg) {
        super(msg);
    }
}
